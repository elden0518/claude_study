def show_interview_qa():
    print("\n\n" + "=" * 70)
    print("  常见面试题 & 解答")
    print("=" * 70)

    qas = [
        {
            "q": "Q1：为什么 Transformer 要用 √d_k 缩放注意力分数？",
            "a": """  A：当 d_k（Key的维度）较大时，Q·K^T 的点积值可能很大。
     极大的值经过 softmax 后会趋向 one-hot 分布（只有最高分接近1，其余趋近0），
     导致梯度几乎为0（梯度消失），模型无法有效学习。
     除以 √d_k 相当于控制温度，使分布更平滑，梯度更稳定。
     例：d_k=64 → 除以 √64=8，缩放了 8 倍。"""
        },
        {
            "q": "Q2：自注意力的计算复杂度是多少？有什么问题？",
            "a": """  A：时间和空间复杂度均为 O(n²·d)，其中 n 是序列长度，d 是维度。
     · 每对词之间都需要计算注意力（n²对）
     · n=512 时还好，但 n=100000 时 n²=1亿，内存爆炸！
     这就是为什么标准 Transformer 上下文窗口有限。
     解决方案：Flash Attention（内存高效）、稀疏注意力（Longformer）、
              线性注意力（Linear Transformer）等改进方法。"""
        },
        {
            "q": "Q3：BERT 和 GPT 都用 Transformer，有什么根本区别？",
            "a": """  A：根本区别在于注意力方向：
     · BERT（Encoder-Only）：双向注意力，每个词能看到左右两侧的词
       → 理解能力强，但无法自回归生成（无法一词一词地生成新文本）
     · GPT（Decoder-Only）：单向注意力（因果掩码），只能看历史
       → 自然支持文本生成，适合对话、续写等任务

     预训练目标也不同：
     · BERT：MLM（预测被遮蔽的词）
     · GPT：LM（预测下一个词）"""
        },
        {
            "q": "Q4：残差连接（Residual Connection）的作用是什么？",
            "a": """  A：两个核心作用：
     1. 防止梯度消失：反向传播时，梯度可以通过 '+x' 直接传到前层，
        不会因多层网络而指数衰减。
     2. 信息保留：即使某层的变换不理想，原始信息通过残差仍可传递，
        最差情况是该层学到 F(x)≈0，相当于跳过该层（什么都不做）。
     公式：output = F(x) + x（而非 output = F(x)）
     使得训练 100+ 层的深度网络成为可能。"""
        },
        {
            "q": "Q5：为什么需要多头注意力？一个头不够吗？",
            "a": """  A：一个注意力头只能在一个语义空间中计算关系，能力有限。
     多头注意力允许模型在多个语义子空间中并行学习不同类型的关系：
     · 头1：学习句法依存关系（主语-谓语-宾语）
     · 头2：学习语义相似性（同义词、反义词）
     · 头3：学习指代关系（'它'指向哪个名词）
     · 头4：学习局部相邻关系（相邻词的模式）

     多头注意力的总参数量和单头相同（因为每头维度 = d_model/h），
     但表达能力远超单头。"""
        },
        {
            "q": "Q6：什么是 Attention Is All You Need？",
            "a": """  A：这是 2017年 Google 发布的论文标题，也是 Transformer 的奠基论文。
     核心主张：之前的 seq2seq 模型用 RNN+Attention，
     论文证明了：可以完全去掉 RNN，只用纯注意力机制（Self-Attention），
     不仅效果更好，而且可以并行训练，速度更快。

     这篇论文直接促成了 BERT、GPT、T5 等一系列大模型的诞生，
     被认为是近代 AI 发展最重要的论文之一。"""
        },
    ]

    for qa in qas:
        print(f"\n  {qa['q']}")
        print(qa['a'])
        print()


